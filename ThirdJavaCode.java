public class ThirdJavaCode{
	public static void main(String[] args){
		float num1, num2, res;
		num1=12f;
		num2=15f;
		num1+num2 = (int) res;
		System.out.println("The added value is = "+res);
		res = num1-num2;
		System.out.println("The subtracted value is = "+res);
		res = num1/num2;
		System.out.println("The divided value is = "+res);
		res = num1*num2;
		System.out.println("The multiplied value is = "+res);
	}
}

